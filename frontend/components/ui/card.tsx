import * as React from "react"

import {cn} from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({className, ...props}, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-xl bg-card text-card-foreground shadow",
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

const CardHeader = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({className, ...props}, ref) => (
  <div
    ref={ref}
    className={cn("flex flex-col space-y-1.5 p-6", className)}
    {...props}
  />
))
CardHeader.displayName = "CardHeader"

interface CardIconHeaderProps extends React.ComponentPropsWithoutRef<typeof CardHeader> {
  icon: React.ReactElement;
}

const CardIconHeader = React.forwardRef<
  React.ElementRef<typeof CardHeader>,
  CardIconHeaderProps
>(({className, children, icon, ...props}, ref) => (
  <CardHeader
    ref={ref}
    {...props}
  >
    <div className="flex items-center gap-2">
      <span className="text-primary/75">{React.createElement(icon.type, {size: 22, ...icon.props})}</span>
      <div>
        {children}
      </div>
    </div>
  </CardHeader>
))
CardIconHeader.displayName = "CardIconHeader"

